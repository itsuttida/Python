
#Loop ซ้อน Loop
#วนทั้งหมด 3 รอบ
#ใน 1 รอบ มี 5 ตำแหน่ง
#รอบที่ 1 ตำแหน่งที่ 1 , รอบที่ 1 ตำแหน่งที่ 2 , ... , รอบที่ 1 ตำแหน่งที่ 5, รอบที่ 2 ตำแหน่งที่ 1


i = 1  #ตัวนับรอบ

while i <= 3 :    # i = 3
    j = 1   
    while j <= 5:  # j = 6
        print("รอบที่ = ",i , "ตำแหน่งที่ = ", j)
        j += 1
    i += 1  # i = i+1